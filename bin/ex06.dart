void main() {
  //제어문 (switch)

  /*
  code = 1
  code 1 --> R101
  code 2 --> R202
  code 3 --> R303
  code 4 --> R404
   */

  int code = 2;

  switch (code) {
    case (1):
      return print("R101");

    case (2):
      return print("R202");
    case (3):
      return print("R303");
    case (4):
      return print("R404");
  }

  
}
